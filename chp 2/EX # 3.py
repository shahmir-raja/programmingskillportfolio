my_name ='\t Sam\n'
#with whitespace
print('Name with white space:')
print(my_name)
#with left strip
print('\nname after left strip:')
print(my_name.lstrip())
#with right strip
print('\nname after right string:')
print(my_name.rstrip())
#with strip
print('\nname strpped from both sides:')
print(my_name.strip())
